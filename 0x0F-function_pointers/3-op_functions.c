#include "3-calc.h"

/**
 * op_add - adds integers
 * @a: integer 1
 * @b: integer 2
 * Return: sum
*/

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subs integers
 * @a: integer 1
 * @b: integer 2
 * Return: sub
*/

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - muls  integers
 * @a: integer 1
 * @b: integer 2
 * Return: mul
*/

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divs integers
 * @a: integer 1
 * @b: integer 2
 * Return: div
*/

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - mods integers
 * @a: integer 1
 * @b: integer 2
 * Return: mod
*/

int op_mod(int a, int b)
{
	return (a % b);
}
